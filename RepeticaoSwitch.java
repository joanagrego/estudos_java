public class RepeticaoSwitch {
    public static void main(String[] args) {

        int mes = 2;

        switch(mes){

            case 1:
                System.out.println("Roger e Daniel");
                break;

            case 2:
                System.out.println("Maria e Malu");
                break;

            default:
                System.out.println("Ninguém faz aniversário");

        }
    }
}
